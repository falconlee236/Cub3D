#include "parse.h"

int	is_data(char *str)
{
	if (ft_strlen(str) < 3)
		return (1);
	if (str[0] == 'N' || str[0] == 'S' || str[0] == 'E' || str[0] == 'W'
		|| str[0] == 'F' || str[0] == 'C')
		return (1);
	return (0);
}
